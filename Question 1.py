# Part 1
x = 10
y = 3
print(f"x = {x}, y = {y}")
# Part 2
print("\nOperations:")
print(f"Multiplication: {x * y}")
print(f"Division: {x / y}")
print(f"Floor division: {x // y}")
print(f"Modulus: {x % y}")
print(f"Exponentiation: {x ** y}")
# Part 3
# Typecasting examples
a = 5
float_a = float(a)
str_a = str(a)
b = "7"
int_b = int(b)
print("\nTypecasting Results:")
print(f"{a} → float: {float_a} ({type(float_a)})")
print(f"{a} → string: '{str_a}' ({type(str_a)})")
print(f"'{b}' → int: {int_b} ({type(int_b)})")
